class color:
    '''
    Colores de texto para la terminal.
    '''
    
    RESET = "\033[0m" # Resetear el color
    rojo = "\033[91m" # Error
    verde = "\033[92m" # Éxito
    amarillo = "\033[93m" # Signo del usuario 1
    azul = "\033[94m" # Preguntas
    purpura = "\033[95m" # Títulos
    cian = "\033[96m" # Opciones, tablero o mensajes informativos
    naranja = "\033[38;5;214m" # Error, pero menos grave